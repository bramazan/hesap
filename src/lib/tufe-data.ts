// Türkçe ay isimleri
export const MONTHS_TR = [
    "Ocak", "Şubat", "Mart", "Nisan", "Mayıs", "Haziran",
    "Temmuz", "Ağustos", "Eylül", "Ekim", "Kasım", "Aralık"
];

// Mevcut yılları döndür
export function getAvailableYears(): number[] {
    const years = new Set<number>();
    Object.keys(TUFE_INDEX).forEach(period => {
        years.add(parseInt(period.split("-")[0]));
    });
    return Array.from(years).sort((a, b) => a - b);
}

// Belirli yıl için mevcut ayları döndür
export function getAvailableMonths(year: number): number[] {
    const months: number[] = [];
    for (let m = 1; m <= 12; m++) {
        const period = `${year}-${String(m).padStart(2, "0")}`;
        if (TUFE_INDEX[period] !== undefined) {
            months.push(m);
        }
    }
    return months;
}

// Belirli dönemin endeks değerini döndür
export function getIndexValue(period: string): number | null {
    return TUFE_INDEX[period] ?? null;
}

// En son açıklanan dönemi döndür
export function getLatestPeriod(): string {
    const periods = Object.keys(TUFE_INDEX).sort();
    return periods[periods.length - 1];
}

// Period'u okunabilir formata çevir
export function formatPeriod(period: string): string {
    const [year, month] = period.split("-");
    return `${MONTHS_TR[parseInt(month) - 1]} ${year}`;
}

// İki tarih arasındaki ay farkını hesapla
export function monthsBetween(startPeriod: string, endPeriod: string): number {
    const [startYear, startMonth] = startPeriod.split("-").map(Number);
    const [endYear, endMonth] = endPeriod.split("-").map(Number);
    return (endYear - startYear) * 12 + (endMonth - startMonth);
}
